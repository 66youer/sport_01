				<el-form-item class="input" v-if="type!='info'"  label="用户姓名" prop="yonghuxingming" >
					<el-input v-model="ruleForm.yonghuxingming" placeholder="用户姓名" clearable  :readonly="ro.yonghuxingming"></el-input>
				</el-form-item>
				<el-form-item v-else class="input" label="用户姓名" prop="yonghuxingming" >
					<el-input v-model="ruleForm.yonghuxingming" placeholder="用户姓名" readonly></el-input>
				</el-form-item>
				<el-form-item class="select" v-if="type!='info'"  label="投票数" prop="toupiaoshu" >
					<el-select :disabled="ro.toupiaoshu" v-model.number="ruleForm.toupiaoshu" placeholder="请选择投票数" >
						<el-option
							v-for="(item,index) in toupiaoshuOptions"
							v-bind:key="index"
							:label="item"
							:value="item">
						</el-option>
					</el-select>
				</el-form-item>
				<el-form-item v-else class="input" label="投票数" prop="toupiaoshu" >
					<el-input v-model="ruleForm.toupiaoshu"
						placeholder="投票数" readonly></el-input>
				</el-form-item>
				<el-form-item class="input" v-if="type!='info'"  label="投票备注" prop="toupiaobeizhu" >
					<el-input v-model="ruleForm.toupiaobeizhu" placeholder="投票备注" clearable  :readonly="ro.toupiaobeizhu"></el-input>
				</el-form-item>
				<el-form-item v-else class="input" label="投票备注" prop="toupiaobeizhu" >
