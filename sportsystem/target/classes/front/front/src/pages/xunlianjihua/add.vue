				<img v-if="ruleForm.tupian.substring(0,4)=='http'" class="upload-img" v-bind:key="index" :src="ruleForm.tupian.split(',')[0]">
				<img v-else class="upload-img" v-bind:key="index" v-for="(item,index) in ruleForm.tupian.split(',')" :src="baseUrl+item">
			</el-form-item>
			<el-form-item class="add-item" label="教练工号" prop="jiaoliangonghao">
				<el-input v-model="ruleForm.jiaoliangonghao" 
					placeholder="教练工号" clearable :disabled=" false  ||ro.jiaoliangonghao"></el-input>
			</el-form-item>
			<el-form-item class="add-item" label="上传时间" prop="shangchuanshijian">
				<el-date-picker
					:disabled=" false  ||ro.shangchuanshijian"
					value-format="yyyy-MM-dd HH:mm:ss"
