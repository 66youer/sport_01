					placeholder="计划名称" clearable :disabled=" false  ||ro.jihuamingcheng"></el-input>
			</el-form-item>
			<el-form-item class="add-item" label="类型" prop="leixing">
				<el-input v-model="ruleForm.leixing" 
					placeholder="类型" clearable :disabled=" false  ||ro.leixing"></el-input>
			</el-form-item>
			<el-form-item class="add-item" label="图片" v-if="type!='cross' || (type=='cross' && !ro.tupian)" prop="tupian">
				<file-upload
					tip="点击上传图片"
